import { Router, Response } from 'express';

const router = Router();

// Health check endpoint
router.get('/', (res: Response) => {
  res.status(200).json({
    status: 'OK',
    timestamp: new Date().toISOString(),
    uptime: process.uptime(),
    environment: process.env.NODE_ENV || 'development',
  });
});

export default router;
